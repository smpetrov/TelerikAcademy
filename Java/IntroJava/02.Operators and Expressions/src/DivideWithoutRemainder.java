/*
 * Write a boolean expression that checks for given integer 
 * if it can be divided (without remainder) by 7 and 5 in the same time.
*/
import java.util.Scanner;
public class DivideWithoutRemainder {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);  
		int x = input.nextInt();
		input.close();
        String result;
        if (((x % 7) == 0) && ((x % 5) == 0))
        {
            result = "can";
        }
        else
        {
            result = "can't";
        }
        System.out.println("Integer " + x + " " + result + " be divided without remainder by 7 an 5 in the same time");
	}

}
